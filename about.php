        <h1 class="index" style="margin-top:0;">
            OUR MISSION
        </h1>
        Sharing Dreams has 3 main goals: To develop teenagers' creative
        side, to develop your humanitarian thinking and to help people in need 
        from all over the world.
        <h1 class="index">
            HOW IT WORKS
        </h1>
        The money raising happens this way: A person posts a picture of their 
        art on the Sharing Dreams' website/app. This art stays publicly exposed, receiving 
        voluntary donations (just 1 US$) at the same time from any person in 
        any part of the planet (by PayPal). The raised money is shared between 
        the four institutions, which use the money to humanitary issues.
        The money that goes to our paypal account is in the name of Guilherme Vieira Rizzo, 
        one of the founders, because we can't officially create the non-profit organization. 
        We need to wait for everyone to be 16 years old  when we'll be emancipated, so it's possible 
        for us to open a bank account. You can see our <a href="http://medium.com/@wesharingdreams" target="_blank">Paypal reports</a> if you want to feel more safe.
